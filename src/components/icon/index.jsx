function _getIconDimensions(height, width) {
  if (height && width) {
    return { height, width };
  }

  if (!height && width) {
    return { height: width, width };
  }

  if (height && !width) {
    return { height, width: height };
  }

  return {
    height: "1em",
    width: "1em",
  };
}

function _getIconAriaData(label) {
  const aria = {};

  if (label) {
    aria["aria-label"] = label;
  } else {
    aria["aria-hidden"] = "true";
  }

  return aria;
}

function Icon(props) {
  const { children, className, fill, height, label, viewBox, width } = props;
  const dimensions = _getIconDimensions(height, width);
  const aria = _getIconAriaData(label);

  const style = {
    display: "inline-block",
    fill: fill || "currentColor",
    height: dimensions.height,
    verticalAlign: "middle",
    width: dimensions.width,
  };

  return (
    <svg
      className={classNames("Icon", className)}
      style={props && props.style ? assign({}, style, props.style) : style}
      viewBox={viewBox}
      {...aria}
    >
      {children}
    </svg>
  );
}

